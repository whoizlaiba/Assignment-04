# Question 1: Sum Two Numbers

def calculate_sum():
    print("== Question 1: Add Two Numbers ==")
    a = int(input("Please enter the first number: "))
    b = int(input("Please enter the second number: "))
    total = a + b
    print(f"The total of {a} and {b} is: {total}")

if __name__ == '__main__':
    calculate_sum()
